package cideplus.model;

public class FeaturerException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public FeaturerException() {
		super();
	}

	public FeaturerException(String message, Throwable cause) {
		super(message, cause);
	}

	public FeaturerException(String message) {
		super(message);
	}

	public FeaturerException(Throwable cause) {
		super(cause);
	}

}
